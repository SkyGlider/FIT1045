import math #imports math module
x=3 #assign 3 to variable x
y=2 #assign 2 to variable y

print( "x + y = " + str(x + y)) #adds x to y
print( "x - y = " + str(x - y)) #subtracts y from x
print( "x * y = " + str(x * y)) #multiplies x and y
print( "x ** y = " + str(x ** y)) #gives x rasied to the power of y
print( "x % y = " + str(x % y)) #gives the integer value of x/y
print( "x / y = " + str(x / y)) #divides x by y
print( "x // y = " + str(x // y)) #gives the remainder of x/y
print( "x > y = " + str(x > y)) #checks if x is greater than y
